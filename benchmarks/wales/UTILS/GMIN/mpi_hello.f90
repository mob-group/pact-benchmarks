PROGRAM HELLO

INCLUDE 'mpif.h'

INTEGER MPIERR, NPAR,MYNODE,J1,J

CALL MPI_INIT(MPIERR)
CALL MPI_COMM_SIZE(MPI_COMM_WORLD,NPAR,MPIERR)
CALL MPI_COMM_RANK(MPI_COMM_WORLD,MYNODE,MPIERR)
J=-2

DO J1=1,10
   PRINT *,'Hello world, I am process ',MYNODE,' of ',NPAR
   CALL MPI_BCAST(J,1,MPI_INTEGER,0,MPI_COMM_WORLD, MPIERR)
   WRITE(*,*) 'here G3 after MPI_BCAST, MYNODE=',MYNODE,' J1=',J1
   CALL FLUSH(6)
ENDDO

CALL MPI_FINALIZE(MPIERR)
    
STOP
END
