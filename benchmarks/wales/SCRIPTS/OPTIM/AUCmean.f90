PROGRAM AUCMEAN
IMPLICIT NONE
INTEGER NFILES, NDATA, J1, J2
DOUBLE PRECISION, ALLOCATABLE :: SUM(:)
DOUBLE PRECISION DUMMY

READ(*,*) NFILES, NDATA
ALLOCATE(SUM(NDATA))
SUM(1:NDATA)=0.0D0

DO J1=1,NFILES
   DO J2=1,NDATA
      READ(*,*) DUMMY
      SUM(J2)=SUM(J2)+DUMMY
   ENDDO
ENDDO
SUM(1:NDATA)=SUM(1:NDATA)/NFILES
WRITE(*,'(G20.10)') SUM(1:NDATA)

END PROGRAM AUCMEAN
