! MODULE SBMDATA 
!***********************************************************************

MODULE SBMDATA
       IMPLICIT NONE
       SAVE

        ! information about atoms
       DOUBLE PRECISION,ALLOCATABLE,DIMENSION(:) :: SBMCHARGE 
       INTEGER,ALLOCATABLE,DIMENSION(:) :: SBMCHARGEON 
       INTEGER :: NUMOFSBMCHARGES 
       INTEGER,ALLOCATABLE,DIMENSION(:) :: SBMRBG,ATOMTYPES
       INTEGER :: NATOMTYPES
       DOUBLE PRECISION,ALLOCATABLE,DIMENSION(:):: STT,SA, SB, SC
       DOUBLE PRECISION,ALLOCATABLE,DIMENSION(:):: XSBM,YSBM,ZSBM 
        ! bond terms
       INTEGER NBA
       INTEGER,ALLOCATABLE,DIMENSION(:) :: IB1,IB2
       DOUBLE PRECISION, ALLOCATABLE, DIMENSION(:) :: RB,BK
        ! angle terms
       INTEGER NTA
       INTEGER,ALLOCATABLE, DIMENSION(:) :: IT,JT,KT 
       DOUBLE PRECISION,ALLOCATABLE,DIMENSION(:) :: ANTC,TK
        ! dihedral terms       
       INTEGER NPA
       INTEGER,ALLOCATABLE,DIMENSION(:) :: IP,JP,KP,LP,PHITYPE
       DOUBLE PRECISION,ALLOCATABLE,DIMENSION(:) :: PHISBM,PK 
        ! contact terms
       INTEGER NC
       INTEGER,ALLOCATABLE,DIMENSION(:) :: IC,JC,CONTACTTYPE
       DOUBLE PRECISION,ALLOCATABLE,DIMENSION(:) :: CONCOEF
        ! non-contact terms
       DOUBLE PRECISION :: NCSWITCH,NCCUT 
       INTEGER,ALLOCATABLE,DIMENSION(:) :: NNCINC,NNEXL1,NNEXL2
       INTEGER MAXSEP,MAXSEPSYS,NEXCLUSIONS 
        ! electrostatic terms 
       INTEGER,ALLOCATABLE,DIMENSION(:) :: NNEXL1ELEC,NNEXL2ELEC
       INTEGER NEXCLUSIONSELEC
       DOUBLE PRECISION :: PREFACTOR,KAPPA,DHswitch,DHcut
        ! position restraint terms
       DOUBLE PRECISION,ALLOCATABLE,DIMENSION(:) :: SBMPRK,SBMPRX
       INTEGER,ALLOCATABLE,DIMENSION(:) :: SBMPRI
       INTEGER SBMPRN
        ! variables to save time in hessian evaluations
       INTEGER SBMHESSATOM
       LOGICAL SBMFIRSTDD
       INTEGER,ALLOCATABLE,DIMENSION(:,:,:) :: SBMNGRID
       INTEGER,ALLOCATABLE,DIMENSION(:,:,:,:) :: SBMGRID
       INTEGER,ALLOCATABLE,DIMENSION(:) :: SBMNB 
       INTEGER,ALLOCATABLE,DIMENSION(:,:) :: SBMBONDLIST 

END MODULE SBMDATA

